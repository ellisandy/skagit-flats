    #[test]
    fn font_data_has_correct_length() {
        assert_eq!(FONT_DATA.len(), 95 * 16);
    }

    #[test]
    fn space_glyph_is_blank() {
        let g = glyph(' ');
        assert!(g.iter().all(|&b| b == 0));
    }

    #[test]
    fn degree_sign_returns_custom_glyph() {
        let g = glyph('\u{00B0}'); // degree sign
        assert_eq!(g, &DEGREE_GLYPH);
    }

    #[test]
    fn non_ascii_returns_replacement() {
        let g = glyph('\u{0400}'); // Cyrillic Е — truly non-ASCII, not degree
        assert_eq!(g, &REPLACEMENT);
    }

    #[test]
    fn font_size_scale_factors() {
        assert_eq!(FontSize::Hero.scale(), 14);
        assert_eq!(FontSize::Large.scale(), 8);
        assert_eq!(FontSize::Medium.scale(), 4);
        assert_eq!(FontSize::Small.scale(), 3);
        assert_eq!(FontSize::Micro.scale(), 2);
    }

    #[test]
    fn font_size_large_dims() {
        // Large: 8× scale, glyph 64×128, cell 72×136
        assert_eq!(FontSize::Large.glyph_w(), 64);
        assert_eq!(FontSize::Large.glyph_h(), 128);
        assert_eq!(FontSize::Large.cell_w(), 72);
        assert_eq!(FontSize::Large.cell_h(), 136);
    }

    #[test]
    fn font_size_medium_dims() {
        // Medium: 4× scale, glyph 32×64, cell 36×68
        assert_eq!(FontSize::Medium.glyph_w(), 32);
        assert_eq!(FontSize::Medium.glyph_h(), 64);
        assert_eq!(FontSize::Medium.cell_w(), 36);
        assert_eq!(FontSize::Medium.cell_h(), 68);
    }

    #[test]
    fn a_glyph_is_not_blank() {
        let g = glyph('A');
        assert!(g.iter().any(|&b| b != 0));
    }
