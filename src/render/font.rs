/// Embedded 8x16 bitmap font for ASCII printable characters (32–126).
///
/// Each glyph is 8 pixels wide and 16 pixels tall. Pixel data is stored as
/// 16 bytes per glyph, with bit 7 of each byte representing the leftmost
/// column. Characters are rendered with 1-pixel spacing on each side,
/// giving a 9x17 cell.
pub const GLYPH_WIDTH: u32 = 8;
pub const GLYPH_HEIGHT: u32 = 16;
/// Cell dimensions include 1px right/bottom spacing.
pub const CELL_WIDTH: u32 = 9;
pub const CELL_HEIGHT: u32 = 17;

/// Font size tiers for the multi-size rendering system.
///
/// Each tier is produced by integer upscaling of the base 8×16 glyph bitmap.
#[derive(Debug, Clone, Copy, PartialEq)]
pub enum FontSize {
    /// 14× scale → ~112×224 px glyphs (≈196 px cap height). Used for GO/NO-GO.
    Hero,
    /// 8× scale → 64×128 px glyphs. Used for temperatures, river level, ferry time.
    Large,
    /// 4× scale → 32×64 px glyphs. Used for flow rate, wind, vessel name.
    Medium,
    /// 3× scale → 24×48 px glyphs. Used for labels, trail/road status, reasons.
    Small,
    /// 2× scale → 16×32 px glyphs. Used for timestamps, site names.
    Micro,
}

impl FontSize {
    /// Integer pixel scale factor relative to the base 8×16 glyph.
    pub fn scale(self) -> u32 {
        match self {
            FontSize::Hero => 14,
            FontSize::Large => 8,
            FontSize::Medium => 4,
            FontSize::Small => 3,
            FontSize::Micro => 2,
        }
    }

    /// Rendered glyph width in pixels (not including inter-glyph spacing).
    pub fn glyph_w(self) -> u32 {
        GLYPH_WIDTH * self.scale()
    }

    /// Rendered glyph height in pixels.
    pub fn glyph_h(self) -> u32 {
        GLYPH_HEIGHT * self.scale()
    }

    /// Cell width including one column of right-side spacing.
    pub fn cell_w(self) -> u32 {
        self.glyph_w() + self.scale()
    }

    /// Cell height including one row of bottom spacing.
    pub fn cell_h(self) -> u32 {
        self.glyph_h() + self.scale()
    }
}

/// Number of glyphs in the font (ASCII 32–126 inclusive).
const GLYPH_COUNT: usize = 95;

/// Degree sign (U+00B0): small circle at top of glyph cell.
static DEGREE_GLYPH: [u8; 16] = [
    0x00, 0x18, 0x24, 0x24, 0x18, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
];

/// Return the 16-byte glyph bitmap for a character, or a filled rectangle
/// for characters outside the printable ASCII range.
pub fn glyph(ch: char) -> &'static [u8; 16] {
    if ch == '\u{00B0}' {
        return &DEGREE_GLYPH;
    }
    let idx = ch as usize;
    if (32..=126).contains(&idx) {
        let offset = idx - 32;
        // Safety: FONT_DATA has exactly GLYPH_COUNT * 16 bytes.
        let start = offset * 16;
        FONT_DATA[start..start + 16].try_into().unwrap()
    } else {
        // Replacement character: filled rectangle
        &REPLACEMENT
    }
}

